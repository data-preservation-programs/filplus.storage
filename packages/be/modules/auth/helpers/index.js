// ///////////////////////////////////////////////////////////////////// Imports
// -----------------------------------------------------------------------------
const { SendData } = require('@Module_Utilities')

// /////////////////////////////////////////////////////////////////// Functions
// -----------------------------------------------------------------------------
// //////////////////////////////////////////////////////////////// Authenticate
const Authenticate = (next) => (req, res) => {
  if (!req.session.identifier) {
    return SendData(res, 403, 'Unauthenticated', false)
  }
  return next(req, res)
}

// ///////////////////////////////////////////////////////////////////// Exports
// -----------------------------------------------------------------------------
module.exports = {
  Authenticate
}
