// /////////////////////////////////////////////////////////////////// Functions
// -----------------------------------------------------------------------------
const Field = (app, store) => {
  return {
    // ===================================================================== get
    get (fieldId) {
      return store.getters['form/fields'].find(field => field.id === fieldId)
    }
  }
}

// ////////////////////////////////////////////////////////////////////// Export
// -----------------------------------------------------------------------------
export default function ({ app, store }, inject) {
  inject('field', Field(app, store))
}
